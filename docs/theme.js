document.addEventListener("DOMContentLoaded", async () => {
  // --- 1. Basic Toggles ---
  const metaTheme = document.getElementById("theme-color-meta");
  const updateMeta = (isDark) => {
    if (metaTheme) metaTheme.setAttribute("content", isDark ? "#1a1b26" : "#f5f5f5"); // Replace with your exact dark/light background hex codes
  };

  const body = document.body;
  if (localStorage.getItem("theme") === "dark") {
    body.classList.add("dark");
    updateMeta(true);
  }

  const themeBtn = document.querySelector(".theme-toggle");
  if (themeBtn) {
    themeBtn.onclick = () => {
      const isDark = body.classList.toggle("dark");
      localStorage.setItem("theme", isDark ? "dark" : "light");
      updateMeta(isDark);
    };
  }

  // --- 2. Right TOC Drawer ---
  const tocBtn = document.querySelector(".toc-toggle");
  const toc = document.getElementById("table-of-contents");

  if (tocBtn && toc) {
    tocBtn.style.display = "inline-block";
    tocBtn.onclick = () => toc.classList.toggle("active");

    // Auto-inject the Header and SVG Close Button into the TOC
    if (!document.querySelector(".toc-header")) {
      const tocHeader = document.createElement("div");
      tocHeader.className = "sidebar-header toc-header";
      tocHeader.innerHTML = "<h3>Contents</h3><button class='toc-close sidebar-close'><svg width='20' height='20' viewBox='0 0 24 24' fill='none' stroke='currentColor' stroke-width='2' stroke-linecap='round' stroke-linejoin='round'><line x1='18' y1='6' x2='6' y2='18'></line><line x1='6' y1='6' x2='18' y2='18'></line></svg></button>";
      toc.insertBefore(tocHeader, toc.firstChild);

      document.querySelector(".toc-close").onclick = () => toc.classList.remove("active");
    }
  } else if (tocBtn) {
    tocBtn.style.display = "none";
  }

  // --- 3. Left Search Drawer ---
  const sidebar = document.getElementById("left-sidebar");
  const sidebarToggle = document.querySelector(".sidebar-toggle");
  const sidebarClose = document.querySelector(".sidebar-close");

  if(sidebarToggle) sidebarToggle.onclick = () => sidebar.classList.add("active");
  if(sidebarClose) sidebarClose.onclick = () => sidebar.classList.remove("active");

  // --- BUG FIX: Bulletproof click-outside logic ---
  document.addEventListener("click", (e) => {
    // Close TOC if active, clicked outside TOC, AND didn't click the TOC toggle
    if (toc && toc.classList.contains("active") && !toc.contains(e.target) && (!tocBtn || !tocBtn.contains(e.target))) {
      toc.classList.remove("active");
    }
    // Close Sidebar if active, clicked outside Sidebar, AND didn't click the Sidebar toggle
    if (sidebar && sidebar.classList.contains("active") && !sidebar.contains(e.target) && (!sidebarToggle || !sidebarToggle.contains(e.target))) {
      sidebar.classList.remove("active");
    }
  });

  // --- 4. Full Content Search ---
  const searchInput = document.getElementById("sidebar-search");
  const tagCloud = document.getElementById("sidebar-tags");
  const sidebarResults = document.getElementById("sidebar-results");

  // Handles paths dynamically so it works locally AND on GitLab Pages
  const basePath = window.location.pathname.includes('/zeroorone') ? '/zeroorone' : '';

  if (searchInput && tagCloud) {
    try {
      // Fetch the JSON generated by Emacs
      const res = await fetch(basePath + '/search.json');
      if (!res.ok) throw new Error("search.json not found");
      const notes = await res.json();

      // Build Tag Cloud from the JSON
      const uniqueTags = new Set();
      notes.forEach(note => {
        if(note.tags && Array.isArray(note.tags)) {
          note.tags.forEach(t => { if(t.trim() !== "") uniqueTags.add(t.trim()); });
        }
      });

      uniqueTags.forEach(tag => {
        const btn = document.createElement("button");
        btn.className = "tag-filter";
        btn.setAttribute("data-tag", tag);
        btn.innerText = "#" + tag;
        tagCloud.appendChild(btn);
      });

      // Render Search Results
      const renderResults = (term) => {
        sidebarResults.innerHTML = "";
        if (!term) return;

        const lowerTerm = term.toLowerCase();
        notes.forEach(note => {
          // Search against FULL TEXT, TITLE, and TAGS
          const matchContent = note.content && note.content.toLowerCase().includes(lowerTerm);
          const matchTitle = note.title && note.title.toLowerCase().includes(lowerTerm);
          const matchTag = note.tags && note.tags.some(t => t.toLowerCase() === lowerTerm);

          if (matchContent || matchTitle || matchTag) {
            const li = document.createElement("li");
            const a = document.createElement("a");
            a.href = basePath + note.url;
            a.innerText = note.title;
            li.appendChild(a);

            if (note.tags && note.tags.length > 0) {
              const tspan = document.createElement("span");
              tspan.className = "result-tags";
              tspan.innerText = " " + note.tags.map(t=>"#"+t).join(" ");
              tspan.style.color = "var(--muted)";
              tspan.style.fontSize = "0.75rem";
              li.appendChild(tspan);
            }
            sidebarResults.appendChild(li);
          }
        });
      };

      searchInput.addEventListener("input", (e) => {
        renderResults(e.target.value);
        document.querySelectorAll(".tag-filter").forEach(b => b.classList.remove("active"));
      });

      tagCloud.addEventListener("click", (e) => {
        if (e.target.classList.contains("tag-filter")) {
          const tag = e.target.getAttribute("data-tag");
          const isActive = e.target.classList.contains("active");
          document.querySelectorAll(".tag-filter").forEach(b => b.classList.remove("active"));

          if (!isActive) {
            e.target.classList.add("active");
            searchInput.value = tag;
            renderResults(tag);
          } else {
            searchInput.value = "";
            renderResults("");
          }
        }
      });

    } catch (err) {
      console.error("Search failed to load. Make sure search.json is being generated.", err);
    }
  }
});
